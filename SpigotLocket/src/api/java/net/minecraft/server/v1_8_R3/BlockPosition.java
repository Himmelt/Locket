package net.minecraft.server.v1_8_R3;

/**
 * @author Himmelt
 */
public abstract class BlockPosition {
    /**
     * Gets x.
     *
     * @return the x
     */
    public abstract int getX();

    /**
     * Gets y.
     *
     * @return the y
     */
    public abstract int getY();

    /**
     * Gets z.
     *
     * @return the z
     */
    public abstract int getZ();
}
